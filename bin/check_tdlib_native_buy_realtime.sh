#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
REPO_ROOT="$(cd "$ROOT_DIR/../.." && pwd)"
PYTHON_BIN="${PYTHON_BIN:-$REPO_ROOT/.venv/bin/python}"
KEEP_WARM_INTERVAL="${KEEP_WARM_INTERVAL:-15}"
TDLIB_ORDER_KEEPWARM_INTERVAL="${TDLIB_ORDER_KEEPWARM_INTERVAL:-5}"
GATE_ITERATIONS="${GATE_ITERATIONS:-100000}"
GATE_TIMEOUT="${GATE_TIMEOUT:-25}"
GATE_LIVE_INJECT_ITERATIONS="${GATE_LIVE_INJECT_ITERATIONS:-9}"

if [[ ! -x "$PYTHON_BIN" ]]; then
  echo "Python executable not found: $PYTHON_BIN" >&2
  exit 1
fi

export PYTHONUNBUFFERED=1
export BYBIT_SPOT_BUY_ENABLED="${BYBIT_SPOT_BUY_ENABLED:-1}"
export BYBIT_QUERY_FILL_AFTER_BUY="${BYBIT_QUERY_FILL_AFTER_BUY:-0}"
export BYBIT_PREFER_CACHED_SYMBOL_CHECK="${BYBIT_PREFER_CACHED_SYMBOL_CHECK:-1}"
export BYBIT_TIMESTAMP_BIAS_MS="${BYBIT_TIMESTAMP_BIAS_MS:--50}"
export BYBIT_CLOCK_GATE_ENABLED="${BYBIT_CLOCK_GATE_ENABLED:-1}"
export LISTING_BYBIT_ORDER_RESPONSE_TIMEOUT_MS="${LISTING_BYBIT_ORDER_RESPONSE_TIMEOUT_MS:-250}"
export LISTING_TDLIB_NATIVE_BUY_ACTIVE="${LISTING_TDLIB_NATIVE_BUY_ACTIVE:-1}"
export LISTING_TDLIB_NATIVE_BUY_ENABLED="${LISTING_TDLIB_NATIVE_BUY_ENABLED:-1}"
export LISTING_TDLIB_NATIVE_KEEPWARM_INTERVAL="${LISTING_TDLIB_NATIVE_KEEPWARM_INTERVAL:-$TDLIB_ORDER_KEEPWARM_INTERVAL}"
export LISTING_TDLIB_NATIVE_SYMBOL_REFRESH_INTERVAL="${LISTING_TDLIB_NATIVE_SYMBOL_REFRESH_INTERVAL:-$KEEP_WARM_INTERVAL}"
export LISTING_TDLIB_NATIVE_ORDER_CLIENT_KEEPWARM_ENABLED="${LISTING_TDLIB_NATIVE_ORDER_CLIENT_KEEPWARM_ENABLED:-1}"
export LISTING_TDLIB_NATIVE_IMMEDIATE_KEEPWARM_REFRESH="${LISTING_TDLIB_NATIVE_IMMEDIATE_KEEPWARM_REFRESH:-1}"
export LISTING_TDLIB_NATIVE_BLOCKING_HOT_ORDER_WARMUP="${LISTING_TDLIB_NATIVE_BLOCKING_HOT_ORDER_WARMUP:-1}"
export LISTING_TDLIB_NATIVE_ASYNC_ORDER_DISPATCH="${LISTING_TDLIB_NATIVE_ASYNC_ORDER_DISPATCH:-1}"
export LISTING_TDLIB_NATIVE_WORKER_SPIN_WAIT="${LISTING_TDLIB_NATIVE_WORKER_SPIN_WAIT:-1}"
export LISTING_TDLIB_NATIVE_WORKER_SPIN_COUNT="${LISTING_TDLIB_NATIVE_WORKER_SPIN_COUNT:-2}"
export LISTING_TDLIB_NATIVE_ORDER_START_SPIN_COUNT="${LISTING_TDLIB_NATIVE_ORDER_START_SPIN_COUNT:-64}"
export LISTING_TDLIB_NATIVE_PARALLEL_KEEPWARM_CLIENTS="${LISTING_TDLIB_NATIVE_PARALLEL_KEEPWARM_CLIENTS:-4}"
export LISTING_TDLIB_NATIVE_ORDER_ON_CACHE_MISS="${LISTING_TDLIB_NATIVE_ORDER_ON_CACHE_MISS:-0}"
export LISTING_TDLIB_NATIVE_REQUIRE_SYMBOL_CACHE="${LISTING_TDLIB_NATIVE_REQUIRE_SYMBOL_CACHE:-1}"
export LISTING_TDLIB_NATIVE_TIMING_ENABLED="${LISTING_TDLIB_NATIVE_TIMING_ENABLED:-0}"
export LISTING_TDLIB_WATCH_CHATS="${LISTING_TDLIB_WATCH_CHATS:--1002562064658:upbit_news,-1001202540487:BithumbExchange}"
export LISTING_TDLIB_SKIP_CLOCK_CALIBRATION="${LISTING_TDLIB_SKIP_CLOCK_CALIBRATION:-1}"
export LISTING_TDLIB_RECEIVE_TIMEOUT_SEC="${LISTING_TDLIB_RECEIVE_TIMEOUT_SEC:-0}"
export LISTING_TDLIB_FLUSH_LISTING_EVENTS="${LISTING_TDLIB_FLUSH_LISTING_EVENTS:-0}"
export LISTING_TDLIB_EMIT_LISTING_EVENTS="${LISTING_TDLIB_EMIT_LISTING_EVENTS:-0}"
export LISTING_NATIVE_AUTO_BUILD="${LISTING_NATIVE_AUTO_BUILD:-0}"
export LISTING_CPP_ULTRA_ENGINE_ENABLED="${LISTING_CPP_ULTRA_ENGINE_ENABLED:-0}"
export LISTING_CLASSIFIER_BACKEND="${LISTING_CLASSIFIER_BACKEND:-python}"

if [[ "${LISTING_TDLIB_RELAY_AUTO_BUILD:-1}" == "1" ]]; then
  if [[ ! -x "$ROOT_DIR/bin/tdlib_json_relay" ]] || \
     [[ "$ROOT_DIR/cpp/tdlib_json_relay.cpp" -nt "$ROOT_DIR/bin/tdlib_json_relay" ]] || \
     [[ "$ROOT_DIR/cpp/build_tdlib_relay.sh" -nt "$ROOT_DIR/bin/tdlib_json_relay" ]]; then
    if ! bash "$ROOT_DIR/cpp/build_tdlib_relay.sh" >/dev/null; then
      echo "TDLib relay build failed; refusing to run readiness gate with a missing or stale relay." >&2
      exit 1
    fi
  fi
fi

if [[ "${LISTING_CLASSIFIER_VERIFY:-1}" == "1" ]]; then
  if ! VERIFY_OUTPUT="$("$PYTHON_BIN" "$ROOT_DIR/bin/verify_listing_classifiers.py" --require-tdlib-relay --require-ultra-engine 2>&1)"; then
    echo "$VERIFY_OUTPUT" >&2
    echo "Listing classifier fixture verification failed; refusing to run readiness gate." >&2
    exit 1
  fi
fi

cd "$ROOT_DIR"

exec "$PYTHON_BIN" "$ROOT_DIR/bin/fast_readiness_gate.py" \
  --strict-live-tdlib \
  --iterations "$GATE_ITERATIONS" \
  --timeout "$GATE_TIMEOUT" \
  --live-inject-iterations "$GATE_LIVE_INJECT_ITERATIONS" \
  "$@"
